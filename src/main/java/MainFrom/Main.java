    private void btnClaAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnClaAddMouseClicked
        try{
            Statement stat = con.createStatement();
            String selectquery = "select * from tbclass where classname='" + txtClaClass.getText().trim() + "'";
            ResultSet rs = stat.executeQuery(selectquery);
            if(rs.next() == true){
                    JOptionPane.showMessageDialog(null, "Class already Create.");                
            }     
            else{
                    String insertclass = "insert into tbclass (department_name,classname,status) values(?,?,?)";
                    PreparedStatement ps = con.prepareStatement(insertclass);
                    
                    ps.setString(1, cboClaDepartmentname.getSelectedItem().toString());
                    ps.setString(2, txtClaClass.getText().trim());
                    ps.setString(3, cboClaStatus.getSelectedItem().toString());
                    
                    ps.executeUpdate();
                    RefreshtbClass();
                    JOptionPane.showMessageDialog(null, "Create Class sucessfully.");              
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnClaAddMouseClicked

